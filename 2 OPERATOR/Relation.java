/*WAP FOR RELATIONAL OPERATOR(<,<=,>,>=)*/

class Relation
{
  public static void main(String args[])
  {
   int x=90;
   int y=110;
   System.out.println(" x > y="  +(x > y));

   System.out.println(" x < y="  +(x < y));

   System.out.println(" x >= y ="  +(x >= y));
   
    System.out.println(" x < = y="  +(x <= y));
  }
}
